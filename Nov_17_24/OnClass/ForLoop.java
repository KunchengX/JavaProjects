public class ForLoop {
    public static void main(String[] args) {
        int n = 5;
        System.out.println("n = " + n);
        for (int i = 0; i < n; i++) {
            System.out.println("Enter for loop, n = " + n + " i = " + i);
            System.out.println("message" + i);
        }
        System.out.println("Quit for loop.");
    }
}
