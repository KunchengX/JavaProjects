package Nov_10_24.Answers;

class AnCar {
    String make;
    String model;
    int year;

    public AnCar(String make, String model, int year) {
        this.make = make;
        this.model = model;
        this.year = year;
    }

    public void displayInfo() {
        System.out.println("Make: " + make);
        System.out.println("Model: " + model);
        System.out.println("Year: " + year);
    }
}
